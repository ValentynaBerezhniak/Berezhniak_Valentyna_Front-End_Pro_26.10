const number1 = Number(prompt('First number'));
const number2 = Number(prompt('Second number'));

const answerplus = (number1 + number2)
const answerminus = (number1 - number2)
const answermultiply = (number1 * number2)
const answerdivide = (number1 / number2)

alert (`${(number1+" + "+number2+" = "+ answerplus)} 
${(number1+" - "+number2+" = "+ answerminus)} 
${(number1+" * "+number2+" = "+ answermultiply)} 
${(number1+" / "+number2+" = "+ answerdivide)}`)
